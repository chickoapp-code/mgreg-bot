#!/bin/bash
# Bash скрипт для запуска тестов (Linux/macOS)

echo "=== Запуск тестов ==="

# Проверка виртуального окружения
if [ ! -d ".venv" ]; then
    echo "Ошибка: Виртуальное окружение не найдено."
    echo "Запустите сначала: ./setup.sh"
    exit 1
fi

# Активация виртуального окружения
echo "Активация виртуального окружения..."
source .venv/bin/activate

# Проверка зависимостей для тестирования
echo "Проверка зависимостей для тестирования..."
if ! pip list | grep -q "pytest"; then
    echo "Установка зависимостей..."
    pip install -r requirements.txt
fi

# Запуск тестов
echo ""
echo "Запуск тестов..."
echo ""

# Запуск pytest с подробным выводом
pytest -v --tb=short

# Проверка результата
if [ $? -eq 0 ]; then
    echo ""
    echo "=== Все тесты прошли успешно! ==="
else
    echo ""
    echo "=== Некоторые тесты провалились ==="
    exit 1
fi

